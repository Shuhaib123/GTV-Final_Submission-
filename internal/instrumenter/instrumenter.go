	// injectCopiesInGoLiterals prepends shadowing copies of the given identifier names
	// at the top of any func literal used as a goroutine within the provided block.
	// Example: for each name "i" it inserts `i := i` as the first statement.
	injectCopiesInGoLiterals := func(block *ast.BlockStmt, names []string) {
		if block == nil || len(names) == 0 {
			return
		}
		// de-dup names and ignore blanks
		uniq := make([]string, 0, len(names))
		seen := make(map[string]bool)
		for _, n := range names {
			if n == "" || n == "_" {
				continue
			}
			if !seen[n] {
				seen[n] = true
				uniq = append(uniq, n)
			}
		}
		if len(uniq) == 0 {
			return
		}
		// local traversal for copying loop vars in go-literals does not need select counters
		usesName := func(b *ast.BlockStmt, name string) bool {
			if b == nil || name == "" {
				return false
			}
			used := false
			ast.Inspect(b, func(n ast.Node) bool {
				if used || n == nil {
					return false
				}
				if _, ok := n.(*ast.FuncLit); ok {
					return false
				}
				if id, ok := n.(*ast.Ident); ok && id.Name == name {
					used = true
					return false
				}
				return true
			})
			return used
		}
		hasShadow := func(b *ast.BlockStmt, name string) bool {
			if b == nil || name == "" {
				return false
			}
			shadow := false
			ast.Inspect(b, func(n ast.Node) bool {
				if shadow || n == nil {
					return false
				}
				if _, ok := n.(*ast.FuncLit); ok {
					return false
				}
				switch t := n.(type) {
				case *ast.AssignStmt:
					if t.Tok == token.DEFINE {
						for _, lhs := range t.Lhs {
							if id, ok := lhs.(*ast.Ident); ok && id.Name == name {
								shadow = true
								return false
							}
						}
					}
				case *ast.RangeStmt:
					if id, ok := t.Key.(*ast.Ident); ok && id.Name == name {
						shadow = true
						return false
					}
					if id, ok := t.Value.(*ast.Ident); ok && id.Name == name {
						shadow = true
						return false
					}
				case *ast.DeclStmt:
					if gd, ok := t.Decl.(*ast.GenDecl); ok {
						for _, spec := range gd.Specs {
							if vs, ok := spec.(*ast.ValueSpec); ok {
								for _, n := range vs.Names {
									if n != nil && n.Name == name {
										shadow = true
										return false
									}
								}
							}
						}
					}
				}
				return true
			})
			return shadow
		}
		renameIdent := func(b *ast.BlockStmt, oldName, newName string) {
			if b == nil || oldName == "" || newName == "" {
				return
			}
			ast.Inspect(b, func(n ast.Node) bool {
				if n == nil {
					return false
				}
				if _, ok := n.(*ast.FuncLit); ok {
					return false
				}
				if id, ok := n.(*ast.Ident); ok && id.Name == oldName {
					id.Name = newName
				}
				return true
			})
		}
		var walkBlock func(*ast.BlockStmt)
		walkBlock = func(b *ast.BlockStmt) {
			for _, st := range b.List {
				switch s := st.(type) {
				case *ast.GoStmt:
					if ce := s.Call; ce != nil {
						if lit, ok := ce.Fun.(*ast.FuncLit); ok && lit.Body != nil {
							// Remove names that are already self-copied at the top of the body
							filter := func(list []string) []string {
								out := make([]string, 0, len(list))
								for _, nm := range list {
									has := false
									// Scan first few statements for `nm := nm`
									scanN := 3
									for i := 0; i < len(lit.Body.List) && i < scanN; i++ {
										if as, ok := lit.Body.List[i].(*ast.AssignStmt); ok && as.Tok == token.DEFINE {
											if len(as.Lhs) == 1 && len(as.Rhs) == 1 {
												if lid, ok := as.Lhs[0].(*ast.Ident); ok && lid.Name == nm {
													if rid, ok := as.Rhs[0].(*ast.Ident); ok && rid.Name == nm {
														has = true
														break
													}
												}
											}
										}
									}
									if !has {
										out = append(out, nm)
									}
								}
								return out
							}
							names2 := filter(uniq)
							if len(names2) == 0 {
								continue
							}
							// Build copies and prepend to body
							copies := make([]ast.Stmt, 0, len(names2))
							for _, nm := range names2 {
								if !usesName(lit.Body, nm) || hasShadow(lit.Body, nm) {
									continue
								}
								newName := gen.next("loop")
								renameIdent(lit.Body, nm, newName)
								copies = append(copies, &ast.AssignStmt{
									Lhs: []ast.Expr{ast.NewIdent(newName)},
									Tok: token.DEFINE,
									Rhs: []ast.Expr{ast.NewIdent(nm)},
								})
							}
							lit.Body.List = append(copies, lit.Body.List...)
						}
					}
				case *ast.BlockStmt:
					walkBlock(s)
				case *ast.IfStmt:
					if s.Body != nil {
						walkBlock(s.Body)
					}
					if b, ok := s.Else.(*ast.BlockStmt); ok {
						walkBlock(b)
					}
				case *ast.ForStmt:
					if s.Body != nil {
						walkBlock(s.Body)
					}
				case *ast.RangeStmt:
					if s.Body != nil {
						walkBlock(s.Body)
					}
				case *ast.SwitchStmt:
					if s.Body != nil {
						for _, cc := range s.Body.List {
							if c, ok := cc.(*ast.CaseClause); ok {
								tmp := &ast.BlockStmt{List: c.Body}
								walkBlock(tmp)
								c.Body = tmp.List
							}
						}
					}
				case *ast.SelectStmt:
					// Traverse into select case bodies to handle go-literal copies
					if s.Body != nil {
						for _, cc := range s.Body.List {
							if c, ok := cc.(*ast.CommClause); ok {
								tmp := &ast.BlockStmt{List: c.Body}
								walkBlock(tmp)
								c.Body = tmp.List
							}
						}
					}
				}
			}
		}
		walkBlock(block)
	}

