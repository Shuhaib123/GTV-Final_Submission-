const instrumentedMarker = "jspt:instrumented"

type nameGen struct {
	counter int
}

func (g *nameGen) next(kind string) string {
	name := fmt.Sprintf("__jspt_%s_%d", kind, g.counter)
	g.counter++
	return name
}

func funcHasSelectorCall(fn *ast.FuncDecl, pkg, name string) bool {
	if fn == nil || fn.Body == nil {
		return false
	}
	found := false
	ast.Inspect(fn.Body, func(n ast.Node) bool {
		if found {
			return false
		}
		call, ok := n.(*ast.CallExpr)
		if !ok {
			return true
		}
		sel, ok := call.Fun.(*ast.SelectorExpr)
		if !ok {
			return true
		}
		id, ok := sel.X.(*ast.Ident)
		if !ok || id.Name != pkg {
			return true
		}
		if sel.Sel != nil && sel.Sel.Name == name {
			found = true
			return false
		}
		return true
	})
	return found
}

