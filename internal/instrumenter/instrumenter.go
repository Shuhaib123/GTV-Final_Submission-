func ensureChPtrInStmt(stmt ast.Stmt, visit func([]ast.Stmt) []ast.Stmt) ast.Stmt {
	switch s := stmt.(type) {
	case *ast.BlockStmt:
		s.List = visit(s.List)
	case *ast.IfStmt:
		if s.Body != nil {
			s.Body.List = visit(s.Body.List)
		}
		switch els := s.Else.(type) {
		case *ast.BlockStmt:
			els.List = visit(els.List)
		case *ast.IfStmt:
			ensureChPtrInStmt(els, visit)
		}
	case *ast.ForStmt:
		if s.Body != nil {
			s.Body.List = visit(s.Body.List)
		}
	case *ast.RangeStmt:
		if s.Body != nil {
			s.Body.List = visit(s.Body.List)
		}
	case *ast.SwitchStmt:
		if s.Body != nil {
			for _, cc := range s.Body.List {
				if c, ok := cc.(*ast.CaseClause); ok {
					c.Body = visit(c.Body)
				}
			}
		}
	case *ast.TypeSwitchStmt:
		if s.Body != nil {
			for _, cc := range s.Body.List {
				if c, ok := cc.(*ast.CaseClause); ok {
					c.Body = visit(c.Body)
				}
			}
		}
	case *ast.SelectStmt:
		if s.Body != nil {
			for _, cc := range s.Body.List {
				if c, ok := cc.(*ast.CommClause); ok {
					c.Body = visit(c.Body)
				}
			}
		}
	}
	return stmt
}

func chPtrBeforeRegion(stmts []ast.Stmt, idx int, ctxName string) ast.Stmt {
	if idx < 0 || idx >= len(stmts) {
		return nil
	}
	if ctxName == "" {
		return nil
	}
	if idx > 0 && isChPtrLog(stmts[idx-1]) {
		return nil
	}
	call := regionCallFromStmt(stmts[idx])
	if call == nil {
		return nil
	}
	chExpr := channelExprFromRegion(call)
	if chExpr == nil {
		return nil
	}
	return EmitChPtrLogWithCtx(chExpr, ctxName)
}

func regionCallFromStmt(stmt ast.Stmt) *ast.CallExpr {
	switch s := stmt.(type) {
	case *ast.ExprStmt:
		return regionCallFromExpr(s.X)
	case *ast.IfStmt:
		if len(s.Body.List) == 1 {
			if inner := regionCallFromStmt(s.Body.List[0]); inner != nil {
				return inner
			}
		}
	}
	return nil
}

func regionCallFromExpr(expr ast.Expr) *ast.CallExpr {
	call, ok := expr.(*ast.CallExpr)
	if !ok {
		return nil
	}
	sel, ok := call.Fun.(*ast.SelectorExpr)
	if !ok {
		return nil
	}
	if id, ok := sel.X.(*ast.Ident); !ok || id.Name != "trace" || sel.Sel.Name != "WithRegion" {
		return nil
	}
	return call
}

func isChPtrLog(stmt ast.Stmt) bool {
	switch s := stmt.(type) {
	case *ast.ExprStmt:
		return isChPtrLogExpr(s.X)
	case *ast.BlockStmt:
		if s == nil || len(s.List) != 1 {
			return false
		}
		return isChPtrLog(s.List[0])
	case *ast.IfStmt:
		if s == nil || s.Else != nil || s.Body == nil || len(s.Body.List) != 1 {
			return false
		}
		return isChPtrLog(s.Body.List[0])
	default:
		return false
	}
}

func isChPtrLogExpr(expr ast.Expr) bool {
	call, ok := expr.(*ast.CallExpr)
	if !ok {
		return false
	}
	sel, ok := call.Fun.(*ast.SelectorExpr)
	if !ok {
		return false
	}
	id, ok := sel.X.(*ast.Ident)
	if !ok || id.Name != "trace" || sel.Sel.Name != "Log" {
		return false
	}
	if len(call.Args) < 3 {
		return false
	}
	if lit, ok := call.Args[1].(*ast.BasicLit); ok {
		return lit.Value == "\"ch_ptr\""
	}
	return false
}

func channelExprFromRegion(call *ast.CallExpr) ast.Expr {
	if len(call.Args) < 3 {
		return nil
	}
	fl, ok := call.Args[2].(*ast.FuncLit)
	if !ok || fl.Body == nil || len(fl.Body.List) == 0 {
		return nil
	}
	first := fl.Body.List[0]
	for {
		if blk, ok := first.(*ast.BlockStmt); ok && len(blk.List) > 0 {
			first = blk.List[0]
			continue
		}
		break
	}
	switch stmt := first.(type) {
	case *ast.SendStmt:
		return stmt.Chan
	case *ast.ExprStmt:
		if ue, ok := stmt.X.(*ast.UnaryExpr); ok && ue.Op == token.ARROW {
			return ue.X
		}
	case *ast.AssignStmt:
		if len(stmt.Rhs) == 1 {
			if ue, ok := stmt.Rhs[0].(*ast.UnaryExpr); ok && ue.Op == token.ARROW {
				return ue.X
			}
		}
	}
	return nil
}
